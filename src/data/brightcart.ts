export const DEMAND_HISTORY = generateDemandHistory();

function generateDemandHistory() {
  const months = [
    'Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun',
    'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec',
  ];
  const seasonality = [0.72, 0.68, 0.85, 0.92, 0.98, 1.0, 0.95, 0.90, 0.95, 1.05, 1.30, 1.45];
  const data: Array<{ month: string; year: number; demand: number; returns: number }> = [];

  const seeds = [
    847, 623, 912, 734, 856, 945, 802, 788, 923, 1045, 1380, 1520,
    892, 651, 945, 768, 891, 978, 834, 812, 956, 1089, 1420, 1580,
  ];

  for (let y = 0; y < 2; y++) {
    for (let m = 0; m < 12; m++) {
      const base = 870;
      const growth = 1 + (y * 12 + m) * 0.006;
      const demand = Math.round(seeds[y * 12 + m] ?? (base * seasonality[m]! * growth));
      data.push({
        month: months[m]!,
        year: 2023 + y,
        demand,
        returns: Math.round(demand * 0.112),
      });
    }
  }
  return data;
}
